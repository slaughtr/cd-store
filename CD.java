class CD {
  public String mArtist;
  public String mAlbum;
  public int mReleaseYear;
  public int mPrice;

  public CD(String inputArtists, String inputAlbum, int inputYear, int inputPrice) {
    mArtist = inputArtists;
    mAlbum = inputAlbum;
    mReleaseYear = inputYear;
    mPrice = inputPrice;
  }
}
